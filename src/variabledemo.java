
public class variabledemo {

	public static void main(String[] args) {
		//variable initialization
		 
		int a = 10;
		long c = 40;
		short d = 50;
		
		//variable usage
		float e = 50.23f;
		double f = 45.65;
		char g = 'k';
		boolean h = true;
		String name = "khushbu rahangdale";
		String dob = "1-aug-1997";
		String sentence = "I am automation engineer";
		System.out.println("Value of A = " + a);
		System.out.println("value of B = " + c);
		System.out.println("value of C = " + d);
		System.out.println("value of D = " + e);
		System.out.println("value of E = " + f);
		System.out.println("value of F = " + g);
		System.out.println("value of g = " + h);
		System.out.println("date of birth = " + dob);
		System.out.println("Name of student =" + name );
		System.out.println("sentence = " + sentence);
		//+ ->concatenation symbol
		
	}

}
