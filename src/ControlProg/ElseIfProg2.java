package ControlProg;

public class ElseIfProg2 {

	public static void main(String[] args) {
		int marks=10;
		if(marks>60 && marks<50)
		{
			System.out.println("Then it take civil branch");
		}
		else if(marks<70 && marks<80)
		{
			System.out.println("Then it take mechanical branch");
		}
		else if(marks<90 && marks<100)
		{
			System.out.println("Then it take cs branch");
		}
		

	}
	

}
