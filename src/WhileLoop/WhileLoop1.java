package WhileLoop;

public class WhileLoop1 {

	public static void main(String[] args) {
		//print odd value for 1-100
		int i=1;
		while(i<=50)
		{
			System.out.print(i+" ");
			i=i+2;
			
		}
		System.out.println();
		System.out.println("----------------------------------------------------");
		int j=2;
		while(j<=50)
		{
			System.out.print(j+" ");
		j=j+2;
		
		}
		System.out.println();
		System.out.println("----------------------------------------------------------");
		int k=1;
		while(k<=20)
		{
			System.out.print(k*k+" ");
			k++;
		}
		System.out.println();
		System.out.println("-----------------------------------------------------------");
		int p=1;
		while(p<=20)
		{
			System.out.print(p*p*p+" ");
			p++;
		}
		System.out.println();
		System.out.println("------------------------------------------------------------");
		int a=30;
		while(a>=1)
		{
			System.out.print(a+" ");
			a--;
		}

	}
	
	

}        
         
         

